package com.guitarfactory.repository;

import com.guitarfactory.domain.entity.Guitar;
import com.guitarfactory.domain.enums.BuildStatus;
import org.springframework.data.jpa.repository.JpaRepository;

import java.util.List;
import java.util.Optional;

public interface GuitarRepository extends JpaRepository<Guitar, Long> {

    Optional<Guitar> findBySerialNumber(String serialNumber);

    List<Guitar> findByBuildStatus(BuildStatus buildStatus);

    List<Guitar> findByModelId(Long modelId);

    List<Guitar> findByCustomerEmail(String customerEmail);
}
